package Domain;

public class Class_List {
	private Long class_id;
	private String class_name;
	private String major;
	private String course;
	private String class_file;
	private String grade_file;

	public Long getClass_id() {
		return class_id;
	}

	public void setClass_id(Long class_id) {
		this.class_id = class_id;
	}

	public String getClass_name() {
		return class_name;
	}

	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}

	public String getMajor() {
		return major;
	}

	public void setMajor(String major) {
		this.major = major;
	}

	public String getCourse() {
		return course;
	}

	public void setCourse(String course) {
		this.course = course;
	}

	public String getClass_file() {
		return class_file;
	}

	public void setClass_file(String class_file) {
		this.class_file = class_file;
	}

	public String getGrade_file() {
		return grade_file;
	}

	public void setGrade_file(String grade_file) {
		this.grade_file = grade_file;
	}
}
